package EnhörningOOP;

public class Bäckahäst {

    static String name;
    static int age;
    static int price;
    static String stable;

    static boolean notDangerous;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public String getStable() {
        return stable;
    }

    public void setStable(String stable) {
        this.stable = stable;
    }

    public boolean isNotDangerous() {
        return notDangerous;
    }

    public void setNotDangerous(boolean notDangerous) {
        this.notDangerous = notDangerous;
    }

}
